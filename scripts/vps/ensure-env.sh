#!/usr/bin/env bash
# Auto-create / repair VPS .env for srv851550 — no manual editing required.
# Usage: bash scripts/vps/ensure-env.sh
set -euo pipefail

ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
ENV_FILE="${ENV_FILE:-$ROOT/.env}"
SECRETS_FILE="${SECRETS_FILE:-$ROOT/.env.secrets}"

gen_secret() { openssl rand -hex 32; }

read_secret() {
  local key="$1"
  if [ -f "$SECRETS_FILE" ]; then
    # shellcheck disable=SC1090
    local val
    val="$(grep -E "^${key}=" "$SECRETS_FILE" 2>/dev/null | head -1 | cut -d= -f2- || true)"
    if [ -n "$val" ]; then
      echo "$val"
      return
    fi
  fi
  if [ -f "$ENV_FILE" ]; then
    local val
    val="$(grep -E "^${key}=" "$ENV_FILE" 2>/dev/null | head -1 | cut -d= -f2- || true)"
    if [ -n "$val" ] && [[ "$val" != *CHANGE_ME* ]] && [ "$val" != "change-me-in-production-min-32-chars" ]; then
      echo "$val"
      return
    fi
  fi
  gen_secret
}

PG_PASS="$(read_secret POSTGRES_PASSWORD)"
JWT_SECRET="$(read_secret JWT_SECRET)"
WEBHOOK_SECRET="$(read_secret WEBHOOK_SIGNING_SECRET)"

mkdir -p "$(dirname "$ENV_FILE")"
cat >"$ENV_FILE" <<EOF
# Auto-generated by scripts/vps/ensure-env.sh — srv851550 / arheb.net
# Re-run: bash scripts/vps/ensure-env.sh (preserves secrets via .env.secrets)

POSTGRES_PASSWORD=${PG_PASS}
DATABASE_URL=postgresql://whatsapp:${PG_PASS}@postgres:5432/whatsapp_sender
REDIS_URL=redis://redis:6379

