API_HOST_PORT=3020
WEB_HOST_PORT=3021

JWT_SECRET=${JWT_SECRET}
JWT_EXPIRES_IN=7d
WEBHOOK_SIGNING_SECRET=${WEBHOOK_SECRET}

CORS_ORIGIN=https://whatsapp.arheb.net
NEXT_PUBLIC_API_URL=https://api.whatsapp.arheb.net
NEXT_PUBLIC_WEB_URL=https://whatsapp.arheb.net

BAILEYS_MOCK=0
WORKER_CONCURRENCY=5

PLATFORM_ADMIN_EMAILS=arheb.invitations@gmail.com
ADMIN_PHONE=201277785111
ADMIN_NOTIFY_ENABLED=1
OTP_DEV_MODE=0
EOF

cat >"$SECRETS_FILE" <<EOF
POSTGRES_PASSWORD=${PG_PASS}
JWT_SECRET=${JWT_SECRET}
WEBHOOK_SIGNING_SECRET=${WEBHOOK_SECRET}
EOF
chmod 600 "$SECRETS_FILE" 2>/dev/null || true

echo "✅ VPS .env ready: $ENV_FILE"
echo "   Secrets persisted: $SECRETS_FILE"
